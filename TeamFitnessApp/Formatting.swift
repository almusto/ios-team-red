import Foundation
import UIKit

extension UIColor {
    
    open class var backgroundBlack: UIColor {
        return UIColor(displayP3Red: 10/255, green: 10/255, blue: 30/255, alpha: 1)
    }
    
    open class var backgroundImageBlue: UIColor {
        return UIColor(displayP3Red: 0/255, green: 66/255, blue: 125/255, alpha: 1)
    }
    
    open class var foregroundOrange: UIColor {
        return UIColor(displayP3Red: 255/255, green: 162/255, blue: 0/255, alpha: 1)
    }
    
}

