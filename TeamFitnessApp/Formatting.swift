import Foundation
import UIKit

extension UIColor {
    
    open class var backgroundBlack: UIColor {
        return UIColor(displayP3Red: 10/255, green: 10/255, blue: 30/255, alpha: 1)
    }
    
    open class var backgroundImageBlue: UIColor {
        return UIColor(displayP3Red: 0/255, green: 66/255, blue: 125/255, alpha: 1)
    }
    
    open class var foregroundOrange: UIColor {
        return UIColor(displayP3Red: 255/255, green: 162/255, blue: 0/255, alpha: 1)
    }
//color set 2
    open class var saltwater: UIColor {
        return UIColor(displayP3Red: 37/255, green: 121/255, blue: 133/255, alpha: 1)
    }
    
    open class var lagoon: UIColor {
        return UIColor(displayP3Red: 94/255, green: 168/255, blue: 167/255, alpha: 1)
    }
    
    open class var whitewash: UIColor {
        return UIColor(displayP3Red: 1, green: 1, blue: 1, alpha: 1)
    }
    
    open class var raspberry: UIColor {
        return UIColor(colorLiteralRed: 1, green: 68/255, blue: 71/255, alpha: 1)
    }
}

