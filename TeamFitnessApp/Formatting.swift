import Foundation
import UIKit

extension UIColor {
    
    open class var backgroundBlack: UIColor {
        return UIColor(displayP3Red: 10, green: 10, blue: 30, alpha: 1)
    }
    
    open class var backgroundImageBlue: UIColor {
        return UIColor(displayP3Red: 0, green: 66, blue: 125, alpha: 1)
    }
    
    open class var foregroundOrange: UIColor {
        return UIColor(displayP3Red: 255, green: 162, blue: 0, alpha: 1)
    }
    
}

